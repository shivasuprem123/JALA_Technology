import java.io.*;
public class DataTypes
{
	public static void main(String[] args)
	{
	    int a=10;
	    float b=12.53f;
	    double dd=19098847;
	    char c='A';
	    boolean bt=true;
	    System.out.println("integer : "+a);
	    System.out.println("float : "+b);
	    System.out.println("double : "+dd);
	    System.out.println("character : "+c);
	    System.out.println("boolean : "+bt);
	    
	}
}
